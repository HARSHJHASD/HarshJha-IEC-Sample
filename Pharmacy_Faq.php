                      prescriptions and over-the-counter medications, and advise physicians, nurses and other health professionals on drug decisions. Pharmacists also provide expertise about the composition of drugs, including their chemical,
                      biological, and physical properties and their manufacture and use. They ensure drug purity and strength and make sure that drugs do not interact in a harmful way. Pharmacists are drug information experts ultimately concerned
                      about their patients' health and wellness.</span></p>
